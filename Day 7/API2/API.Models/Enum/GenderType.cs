namespace API.Models.Enum;

/// <summary>
/// Represents the gender of a member.
/// </summary>
public enum GenderType
{
    /// <summary>
    /// Represents an unknown gender.
    /// </summary>
    Unknown = 0,

    /// <summary>
    /// Represents a male gender.
    /// </summary>
    Male = 1,

    /// <summary>
    /// Represents a female gender.
    /// </summary>
    Female = 2,

    /// <summary>
    /// Represents a gender other than male or female.
    /// </summary>
    Other = 3
}